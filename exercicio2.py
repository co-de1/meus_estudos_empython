import math

num1: int = int(input("\nEnter the first number: "))
num2: int = int(input("\nEnter the second number: "))

if num1 > 0:
    num1: float = math.sqrt(num1)
    print(f"\nThe square root is: {num1}")
else:
    num2: float = math.sqrt(num2)
    print(f"\nThe square root is:: {num2}")

